					<h4>1. Purpose and scope</h4>
					<p>This procedure ensures that effective internal audits are carried out, as a means of maintaining the effectiveness of the management system.</p>
                    <h4 class="m-t-20">2. Responsibility</h4>
                    <p>The Director decides who is to be appointed as auditors. Whilst ideally auditors should be members of the organisation, some or all of the audits may be subcontracted to a suitably qualified and experienced subcontract person or consultancy.</p>
                    <p>The Director ensures that auditors are trained and are competent, draws up the audit schedule, progresses corrective and preventive actions found to be necessary as a result of an audit, and reports to the management review meeting.</p>
                    <p>Auditors carry out audits in a comprehensive and responsible manner, and prepare audit findings reports for discussion with local management.</p>
                    <h4 class="m-t-20">3. Auditors</h4>
                    <p>Auditors shall have received training in the responsibilities and skills of internal auditing. Auditors, if possible, shall be managerially independent of the activity being audited.<br>
					Auditors shall be alert to the impact of the activities they are auditing and shall draw the attention of the Director to any aspect which they feel is not adequately represented.</p>
                    <h4 class="m-t-20">4. Audit schedule</h4>
                    <p>All processes shall be included within the auditing system.<br>
					The electronic system shall automatically detail the frequency of audits. The frequency of audits shall be based on the importance of the topic.<br>
					Every topic shall be audited at least once a year. Repeat audits shall be scheduled at any time if an audit shows significant shortcomings which need to be corrected quickly. Audits shall be carried out within a reasonable timeframe of the scheduled month.</p>
                    <h4 class="m-t-20">5. The audit</h4>
                    <p>Auditors shall first review the outcome of any previous audit of the topic and check that there are no outstanding corrective or preventive actions. Auditors shall plan the audit using information from the relevant clauses of the Management Manual, the relevant Operating Procedures (and any associated Work Instructions).<br>
					Auditors shall seek evidence to test the requirements of the management system are being fulfilled. This evidence shall be documented in the audit report and be of
					sufficient depth that the information can be re-traced if necessary.</p>
					<h4 class="m-t-20">6. Audit report</h4>
                    <p>Auditors shall record in the Audit Report the reference to the part of the system which has been audited, the evidence examined and the outcome.
					Findings shall be categorised in the report. The audit templates are constructed in a manner to allow entire clauses
					to be audited simultaneously. For example, the evidence associated with
					section 8.7 Control of nonconforming outputs, will refer to the whole of
					8.7 (8.7.1 and 8.7.2) unless there is a more specific question that has
					been deemed appropriate.</p>
					<h4 class="m-t-20">7. Corrective action</h4>
                    <p>The Director shall ensure that any short term corrective actions are carried out promptly and sign them off on the Report.</p>
                    <h4 class="m-t-20">8. Preventive actions</h4>
                    <p>Long term corrective and preventive actions shall be agreed with the relevant directors, managers or supervisors.<br>
					The agreed actions shall be written into a Corrective/Preventive Action Form which shall be sent to the person responsible for taking action. If the audit finding indicates a weakness or mistake within the system, the system administrator shall be notified and Operating Procedures shall be changed to correct the situation.</p>
                    <h4 class="m-t-20">9. Follow-up</h4>
                    <p>The Director shall in due course verify that actions have been effective, and sign off the verification box.</p>
					<h4 class="m-t-20">10. Report to management</h4>
                    <p>The Director shall prepare a brief report summarising audits carried out, the findings and resulting corrective actions and submit it to each management review meeting for discussion.</p>
